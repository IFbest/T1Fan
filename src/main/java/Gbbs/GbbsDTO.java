package Gbbs;

public class GbbsDTO {
   private int gbbsId;
   private String gbbsTitle;
   private String id;
   private String gbbsDate;
   private String gbbsContent;
   private int gbbsAvailable;
   
   public int getGbbsId() {
      return gbbsId;
   }
   public void setGbbsId(int gbbsId) {
      this.gbbsId = gbbsId;
   }
   public String getGbbsTitle() {
      return gbbsTitle;
   }
   public void setGbbsTitle(String gbbsTitle) {
      this.gbbsTitle = gbbsTitle;
   }
   public String getId() {
      return id;
   }
   public void setId(String id) {
      this.id = id;
   }
   public String getGbbsDate() {
      return gbbsDate;
   }
   public void setGbbsDate(String gbbsDate) {
      this.gbbsDate = gbbsDate;
   }
   public String getGbbsContent() {
      return gbbsContent;
   }
   public void setGbbsContent(String gbbsContent) {
      this.gbbsContent = gbbsContent;
   }
   public int getGbbsAvailable() {
      return gbbsAvailable;
   }
   public void setGbbsAvailable(int gbbsAvailable) {
      this.gbbsAvailable = gbbsAvailable;
   }   
}